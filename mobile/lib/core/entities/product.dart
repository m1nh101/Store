class Product {
  String id;
  String name;
  String brand;
  double price;

  Product(this.id, this.name, this.brand, this.price);
}

class ProductItem {
  String id;
  String color;
  String size;
  int quantity;
  double additionPrice;

  ProductItem(this.id, this.color, this.size, this.quantity, this.additionPrice);
}